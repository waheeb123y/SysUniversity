    public boolean DB() {
        boolean isAdded = false;
        id = Integer.parseInt(stu_id1.getText());
        Name = stu_name.getText();
        mejor2 = mejor.getText();
        address = addres.getText();
        phone2 = Integer.parseInt(phone.getText());

        try {
            PreparedStatement pst;
            String sql = "insert into studentu (id,  name , phone, mejor, email) values(? , ? ,? , ?,?)";
            pst = con.prepareStatement(sql);
            pst.setInt(1, id);
            pst.setString(2, Name);
            pst.setInt(3, phone2);
            pst.setString(4, mejor2);
            pst.setString(5, address);
            
            int updateRowCount = pst.executeUpdate();
            if (updateRowCount > 0) {
                isAdded = true;
//                JOptionPane.showMessageDialog(null, "Added");
            } else {
//                JOptionPane.showMessageDialog(null, "Failed adding");
                isAdded = false;
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage()
            );
        }
        return true;
    }
