    public boolean checkadd() {
        if (stu_id1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter ID");
            return false;
        } else if (stu_name.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Student Name ");
            return false;
        } else if (mejor.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Major");
            return false;
        }
        return true;
    }
