    public void setStidentToTable() {

        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from studentu");
            while (rs.next()) {
                //when add row , all data ara adding again . possible to make if statement 
                // here we make function to delete all data and add with new row
                 id = rs.getInt("id");
                Name = rs.getString("name");
                mejor2 = rs.getString("mejor");
                address = rs.getString("gender");
                phone2 = rs.getInt("phone");
                Object ob[] = {id, Name, mejor2,address, phone2};
                model = (DefaultTableModel) bookTable.getModel();
                model.addRow(ob);
            }

//            PreparedStatement pst = con2.prepareStatement("select * from books");
        } catch (HeadlessException | SQLException ex) {
            Logger.getLogger(addStu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public boolean update() {
        boolean isUpdated = false;
        id =Integer.parseInt( stu_id1.getText());
        Name = stu_name.getText();
        mejor2 = mejor.getText();
        address = addres.getText();
        phone2 = Integer.parseInt(phone.getText());
        try {
            Connection con = connection.connect();
            String sql = "update studentu set  name = ? , mejor = ? , address = ?  where id= ?";
            PreparedStatement prs = con.prepareStatement(sql);
            prs.setString(1, Name);
            prs.setString(2, mejor2);
            prs.setString(3, address);
            prs.setInt(4, id);
            prs.executeUpdate();
            int rowCount = prs.executeUpdate();
            if (rowCount > 0) {
                JOptionPane.showMessageDialog(null, "Done");
                isUpdated = true;
            } else {
                JOptionPane.showMessageDialog(null, "Failed");
                return false;
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        return true;
    }
    
    public boolean delete(){
        id  = Integer.parseInt(stu_id1.getText());
        boolean isdeleted= false;
        try {
             Connection con = connection.connect();
            String sql = "delete from studentu   where id= ?";
            PreparedStatement prs = con.prepareStatement(sql);
            
            prs.setInt(1, id);
           
            int rowCount = prs.executeUpdate();
            if (rowCount > 0) {
                JOptionPane.showMessageDialog(null, "Done");
                isdeleted = true;
            } else {
                JOptionPane.showMessageDialog(null, "Failed");
                
                isdeleted = false;
            }
        } catch (Exception e) {
        }
        
        return isdeleted;
    }

